import turtle
def square(pigment):
    peaches = turtle.Turtle()
    peaches.color(pigment)
    peaches.forward(100)
    peaches.right(90)
    peaches.forward(100)
    peaches.right(90)
    peaches.forward(100)
    peaches.right(90)
    peaches.forward(100)

square("gold")
